    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
        
        if(selecciona.showDialog(this, "Abrir archivo") == JFileChooser.APPROVE_OPTION){
           SeleccionarArchivo = selecciona.getSelectedFile();
           if(SeleccionarArchivo.canRead());
           if(SeleccionarArchivo.getName().endsWith("txt")){
               
               String contenido = fc.Abrir(SeleccionarArchivo);
               jTextArea1.setText(contenido);
           }else{
               JOptionPane.showMessageDialog(null,"Archivo invalido");
           }
        }
        
    }//GEN-LAST:event_jMenuItem8ActionPerformed


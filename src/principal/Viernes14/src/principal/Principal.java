    private void jMenuItem11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem11ActionPerformed
        if(selecciona.showDialog(null, "GUARDAR COMO") == JFileChooser.APPROVE_OPTION){
            SeleccionarArchivo = selecciona.getSelectedFile();
                if(SeleccionarArchivo.getName().endsWith("txt")){
                String contenido = jTextArea1.getText();
                String respuesta = fc.Guardar(SeleccionarArchivo, contenido);
                    if(respuesta!=null){
                        JOptionPane.showMessageDialog(null, respuesta);
                    }else{
                    JOptionPane.showMessageDialog(null,"Error al guardar el texto");
                    }
                }
        }
    
    }//GEN-LAST:event_jMenuItem11ActionPerformed

